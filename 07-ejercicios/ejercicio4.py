"""
Pedir dos numeros al usuario y hacer todas las operaciones
de una calculadora. Suma, resta, multiplicacion y division.
"""

nro1 = int(input("Ingrese Nro1: "))
nro2 = int(input("Ingrese Nro2: "))

print(f"{nro1} + {nro2} = {nro1+nro2}")
print(f"{nro1} - {nro2} = {nro1-nro2}")
print(f"{nro1} * {nro2} = {nro1*nro2}")
print(f"{nro1} / {nro2} = {nro1/nro2}")